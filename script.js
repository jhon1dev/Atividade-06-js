// Ao clicar no botão, a fonte, o tamanho da fonte e a cor do texto do parágrafo devem ser
// alterados.
var texto = document.getElementById("text");

function js_style() {
  texto.style.fontFamily = "Verdana, Geneva, Tahoma, sans-serif";
  texto.style.fontSize = "40px";
  texto.style.color = "orange";
}
